#ifndef SINGLETON_HPP
#define	SINGLETON_HPP

namespace cep {

    /** http://blog.csdn.net/haoel/archive/2009/03/26/4028232.aspx */
    template<typename T> class singleton {
    public:

        static T& instance() {
            static T instance_; // Must have a default constructor
            return instance_;
        }
    };

    // factory!!!
}

#endif	/* SINGLETON_HPP */
